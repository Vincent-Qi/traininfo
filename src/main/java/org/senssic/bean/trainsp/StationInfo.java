package org.senssic.bean.trainsp;

public class StationInfo {
	private String name;
	private String spfull;
	private String spjian;
	private String station;

	public String getStation() {
		return station;
	}

	public void setStation(String station) {
		this.station = station;
	}

	
	
	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getSpfull() {
		return spfull;
	}

	public void setSpfull(String spfull) {
		this.spfull = spfull;
	}

	public String getSpjian() {
		return spjian;
	}

	public void setSpjian(String spjian) {
		this.spjian = spjian;
	}

}
